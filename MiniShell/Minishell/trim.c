#include "minishell.h"

char	*ft_strtttrim(char *s)
{
	int i;
	int n;

	n = ft_strlen(s);
	i = 0;
	while (i < n && s[i])
	{
		if (s[i] == ' ' && s[i + 1] == ' ')
			ft_strcpy(s + i, s + i + 1);
		else
			i++;
	}
	return (s);
}
