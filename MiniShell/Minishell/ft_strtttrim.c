#include "minishell.h"

void	ft_trim(char *s)
{
	int i;

	i = 0;
	while (s[i])
	{
		if ((s[i] == ' ' || s[i] == '\t' || s[i] == '\n') && (s[i + 1] == ' ' || s[i + 1] == '\t' || s[i + 1] == '\n'))
			ft_strcpy(s + i, s + i + 1);
		else
			i++;
	}
	i = 0;
	while (s[i])
		i++;
	i--;
	while (s[i] == ' ' || s[i] == '\t' || s[i] == '\n')
		i--;
	i++;
	while (s[i] == ' ' || s[i] == '\t' || s[i] == '\n')
		ft_strcpy(s + i, s + i + 1);
}
